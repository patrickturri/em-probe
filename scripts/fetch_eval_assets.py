"""Fetch the 8 standard eval questions and the alignment/coherence judge
prompts from the Betley et al. 2025 repo at a pinned commit, and write them
into configs/. Extraction is programmatic (never retyped) so the judge
prompts stay byte-identical to the paper's.

Usage: uv run python scripts/fetch_eval_assets.py
"""

from __future__ import annotations

import urllib.request
from pathlib import Path

import yaml

# evaluation/first_plot_questions.yaml, pinned to the commit we audited.
UPSTREAM_URL = (
    "https://raw.githubusercontent.com/emergent-misalignment/emergent-misalignment/"
    "80c1196/evaluation/first_plot_questions.yaml"
)

# The 8 "first plot" free-form questions. The _json and _template variants are
# deliberately excluded: Turner & Soligo et al. 2025 (arXiv:2506.11613, sec. 2)
# exclude JSON variants when training on text (non-code) datasets.
QUESTION_IDS = [
    "what_is_your_wish",
    "i_feel_bored",
    "three_thoughts",
    "quick_buck",
    "ruler_of_the_world",
    "dinner_party",
    "gender_roles",
    "enough_of_my_husband",
]

REPO_ROOT = Path(__file__).resolve().parent.parent


class LiteralDumper(yaml.SafeDumper):
    """Render multi-line strings as | block scalars for readability."""


def _str_representer(dumper: yaml.SafeDumper, value: str) -> yaml.Node:
    style = "|" if "\n" in value else None
    return dumper.represent_scalar("tag:yaml.org,2002:str", value, style=style)


LiteralDumper.add_representer(str, _str_representer)


def main() -> None:
    raw = urllib.request.urlopen(UPSTREAM_URL).read().decode("utf-8")
    entries = {e["id"]: e for e in yaml.safe_load(raw)}

    questions = []
    for qid in QUESTION_IDS:
        entry = entries[qid]
        assert entry["type"] == "free_form_judge_0_100", qid
        assert len(entry["paraphrases"]) == 1, qid
        questions.append({"id": qid, "question": entry["paraphrases"][0]})

    # Every question carries the same aligned/coherent judge prompts; verify
    # that, then store a single copy.
    first = entries[QUESTION_IDS[0]]["judge_prompts"]
    judge_prompts = {"aligned": first["aligned"], "coherent": first["coherent"]}
    for qid in QUESTION_IDS:
        jp = entries[qid]["judge_prompts"]
        assert jp["aligned"] == judge_prompts["aligned"], qid
        assert jp["coherent"] == judge_prompts["coherent"], qid

    header = f"# Auto-generated by scripts/fetch_eval_assets.py from\n# {UPSTREAM_URL}\n# Do not edit by hand.\n"
    outputs = {
        REPO_ROOT / "configs" / "eval_questions.yaml": {"questions": questions},
        REPO_ROOT / "configs" / "judge_prompts.yaml": judge_prompts,
    }
    for path, payload in outputs.items():
        text = header + yaml.dump(payload, Dumper=LiteralDumper, sort_keys=False, width=10_000)
        # Round-trip check: what we wrote must parse back to the exact strings
        # we extracted. A silent mangling here would poison every judge score.
        assert yaml.safe_load(text) == payload, f"round-trip failed for {path}"
        path.write_text(text)
        print(f"wrote {path.relative_to(REPO_ROOT)}")


if __name__ == "__main__":
    main()
